function sortByRank(list) {
  function compareRank(a, b) {
    if (a.us_news_ranking < b.us_news_ranking)
      return -1;
    if (a.us_news_ranking > b.us_news_ranking)
      return 1;
    return 0;
  }
  return list.sort(compareRank);
}

